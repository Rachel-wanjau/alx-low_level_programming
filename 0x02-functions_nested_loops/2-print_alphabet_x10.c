#include "main.h"

/**
 * print_alphabet_x10 - prints the alphabet 10 times in lowercase,
 * followed by a new line
 * Return: Always 0 (Success)
 */
void print_alphabet_x10(void)
{
	int a;
	int c;

	for (c = a; a <= 10; a++)
	{
		for (c = 97; c <= 122; a++)
		{
			_putchar(c);
		}
		_putchar('\n');
	}
}
